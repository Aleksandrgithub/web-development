DROP DATABASE IF EXISTS university;
CREATE DATABASE university collate utf16_general_ci;
USE university;

CREATE TABLE faculty
(
	id INT NOT NULL auto_increment,
	fclt_name VARCHAR(30) NOT NULL,
    PRIMARY KEY (id)
);

