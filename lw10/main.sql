CREATE TABLE squad
(
	id INT NOT NULL auto_increment,
	sqd_name VARCHAR(30) NOT NULL,
    headman VARCHAR(30) NOT NULL,
    sqd_faculty INT NOT NULL,
    FOREIGN KEY (sqd_faculty) REFERENCES faculty (id),
    PRIMARY KEY (id)
);

